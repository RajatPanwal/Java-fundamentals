
public class loops {

    public static void main(String[] args){

        // while
        int i = 1;
        while(i < 5){
            System.err.println(i);
            i++;
        }

        // for loop
        for(int j=0; j<5; j++){
            System.err.println(j);
        }

        // do while
        do{
            System.err.println(i);
        }while(i<0);

    }

}